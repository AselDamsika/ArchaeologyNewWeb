		<p>Geography is an invaluable discipline in providing both national and global literacy and has now been recognized as a compulsory subject from grade 6 to grade 11 in schools.  However, the Department of Education has experienced difficulties in finding qualified teachers at school levels.  As a solution to this problem, The Provincial Ministry of Education and The Department of Geography of The University of Peradeniya started The Certificate Course for school teachers in 2011. The Provincial Ministry of Education finances this course. Geography teachers in schools from grades 6 to 11 is the primary target group of this course. This is a six month course held during the weekends both in Sinhala and Tamil media. The aims of the Certificate Course in Geography for School Teachers are as follows.</p>        
